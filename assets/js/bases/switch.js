

const dia = 2 // 0:domingo; 1: lunes; 2: martes

switch( dia ) {
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miércoles');
        break;
    default:
        console.log('No es lunes, martes, miércoles o domingo');
}






